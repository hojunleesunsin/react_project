CREATE TABLE MST_EMPLOYEE (
  EMPLOYEE_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
  LOGIN_ID VARCHAR2(50) NOT NULL,
  EMAIL VARCHAR2(255) NOT NULL,
  FULL_NAME VARCHAR2(100) NOT NULL,
  PASSWORD_HASH VARCHAR2(255) NOT NULL,
  PHONE_NUMBER VARCHAR2(30),
  DEPARTMENT_NAME VARCHAR2(100),
  POSITION_TITLE VARCHAR2(100),
  HIRE_DATE DATE,
  IS_ACTIVE CHAR(1) DEFAULT 'Y' NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT UQ_MST_EMPLOYEE_LOGIN_ID UNIQUE (LOGIN_ID),
  CONSTRAINT UQ_MST_EMPLOYEE_EMAIL UNIQUE (EMAIL),
  CONSTRAINT CHK_MST_EMPLOYEE_IS_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);
